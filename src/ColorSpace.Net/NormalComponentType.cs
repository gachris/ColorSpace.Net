namespace ColorSpace.Net;

/// <summary>
/// Enum representing the hue component in the HSV color space.
/// </summary>
public enum NormalComponentType
{
    /// <summary>
    /// Hue component in the HSV color space.
    /// </summary>
    Hsv_H,

    /// <summary>
    /// Saturation component in the HSV color space.
    /// </summary>
    Hsv_S,

    /// <summary>
    /// Value component in the HSV color space.
    /// </summary>
    Hsv_V,

    /// <summary>
    /// Red component in the RGB color space.
    /// </summary>
    Rgb_R,

    /// <summary>
    /// Green component in the RGB color space.
    /// </summary>
    Rgb_G,

    /// <summary>
    /// Blue component in the RGB color space.
    /// </summary>
    Rgb_B,

    /// <summary>
    /// Lightness component in the LAB color space.
    /// </summary>
    Lab_L,

    /// <summary>
    /// A component in the LAB color space.
    /// </summary>
    Lab_A,

    /// <summary>
    /// B component in the LAB color space.
    /// </summary>
    Lab_B
}
